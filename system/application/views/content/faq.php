      <h2>What does Feedvolley do?</h2>
      <p><b>Feedvolley lets you display any RSS/Atom content with your own customizable HTML</b>. You can use any of the 
      pre-existing HTML templates or you can modify them or create your own HTML template from scratch. Templates can 
      consist of any HTML, Javascript and CSS you like, together with Feedvolley's own markup. <b>Feedvolley's markup is 
      largely based on <a href="http://www.tumblr.com/help/custom_themes">Tumblr's markup</a></b>, so you can easily 
      <a href="http://freethemes.tumblr.com/">fit</a>
      <a href="http://customthemes.tumblr.com/">any</a> 
      <a href="http://heather-rivers.com/post/30228374/tumblr-themes">of</a>
      <a href="http://parandroid.com/gallery/tag/free-tumblr-themes">the</a>
      <a href="http://www.heavy-backpack.com/archives/articles/top-10-best-tumblr-designs-themes">existing</a> 
      <a href="http://www.tumblr.com/browse/designs">Tumblr</a>
      <a href="http://tumblrthemes.com/">themes</a> for use with Feedvolley.</p>
    </div>

    <div class="text">
      <h2>What is it useful for?</h2>
      <p>You can <b>present content from existing websites with your own layout</b> (this requires that the original site 
      has an RSS feed - most websites do). Also, you can <b>present content created with RSS mixing/modifying apps</b> like 
      Yahoo! Pipes or similar, which allow aggregating, mixing and transforming RSS feeds. And you can probably do other 
      stuff we didn't think of too. You have total control of the front end, so run free.
      </p>
    </div>

    <div class="text">
      <h2>Why do you ask for my email?</h2>
      <p>Feedvolley doesn't require creating a user account to build your pages. Instead we email you a 'secret key' URL
      you can use to edit your pages - this why we ask for your email. The key is kept in a browser cookie too, so you can
      start customizing your page right after you create it. The email is just for backup. <i><b>We will not give out your
      email to anyone or use it for any other purpose</b></i>. We hate spam too. (And we also hate having to create user 
      account to use web apps)</p>
    </div>

    <div class="text">
      <h2>Can't I build the same applications without using Feedvolley?</h2>
      <p>Yes you can. But Feedvolley allows non-techies build pages that would require programming, front-end developers to 
      achieve results that would normally require backend code (eg, PHP or Rails) and programmers to build stuff without 
      having to code, maintain and host a website.
      </p>
